require 'pry'

class String

  def sentence?
    if self.end_with?(".")
      true
    else
      false
    end
  end

  def question?
    if self.end_with?("?")
      true
    else
      false
    end
  end

  def exclamation?
    if self.end_with?("!")
      true
    else
      false
    end
  end

  def count_sentences
    each_sentence = self.split(/[.!?...]/)
    each_sentence.delete_if do |sentence|
      sentence.empty?
    end
    each_sentence.length
  end

end