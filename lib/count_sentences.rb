require 'pry'

class String

  def sentence?
    if self.end_with?(".")
      true
    else
      false
    end
  end

  def question?
    if self.end_with?("?")
      true
    else
      false
    end
  end

  def exclamation?
    if self.end_with?("!")
      true
    else
      false
    end
  end

  def count_sentences

    temp = self.split(/[.!?...]/)
    temp.delete_if do |word|
      word.empty?
    end
    temp.count
  end

end
